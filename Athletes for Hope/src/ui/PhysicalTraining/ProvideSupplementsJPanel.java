    private void populatereport() {
        Date date = new Date();
        pRequest.setRequestDate(date);
        jTextField2.setText(date.toString());
        jTextField1.setText(pRequest.getSender().toString());
        jTextField4.setText(pRequest.getTrainingCoachWorkRequest().getTalentScoutWorkRequest().getChildName());
        populatetable();
        
    }

    private void populatetable() {
        DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
        Object[] row=new Object[2];
        model.setRowCount(0);
        
         for(Supplements M : pRequest.getSupplements())
         {
         
            row[0]=M;
            row[1]=M.getAmount();
            
            model.addRow(row);
            
        }
    }
